function fillPopulationArray(startF,startM,intervals){
    var array = [];
    var i = 0;
    do {
        array.push("POB"+startF);
        array.push("POB"+startM);
        startF++;
        startM++;
        i++;
    } while (i <= intervals);
    return array
}